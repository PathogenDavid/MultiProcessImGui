void SubmitMyWindow()
{
    ImGui::Begin("Server Window");
    ImGui::Text("This is a window from the server process");
    ImGui::Text("PID: %d", GetCurrentProcessId());
    ImGui::End();
}

void SubmitClientWindows()
{
    Server_SubmitClients();
}

